def affiche_role_developpeur(langage):
    if langage == "JavaScript":
        print("tu es un développeur web")
    elif langage == "python":
        print("tu es un développeur IA")
    elif langage == "java":
        print("tu es un développeur logiciel")
    

affiche_role_developpeur("JavaScript")  
affiche_role_developpeur("python")  
affiche_role_developpeur("java")  
